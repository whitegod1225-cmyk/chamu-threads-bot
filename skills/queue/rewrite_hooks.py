def rewrite(text: str) -> str:
    def replace_block(match):
        block = match.group(0)
        # 投稿番号を取得
        num_m = re.match(r'## 投稿(\d+)', block)
        if not num_m:
            return block
        num = num_m.group(1)
        if num not in HOOKS:
            return block

        new_hook = HOOKS[num]

        # **本文** の直後の最初の行を差し替える
        # パターン: **本文**\n{空行}\n{1行目}\n
        def replace_first_line(m):
            prefix = m.group(1)   # **本文**\n\n
            old_first = m.group(2)  # 古い1行目
            return prefix + new_hook

        block = re.sub(
            r'(\*\*本文\*\*\n\n?)([^\n]+)',
            replace_first_line,
            block,
            count=1
        )
        return block

    # ## 投稿NNN から次の ## 投稿 or ファイル末尾まで
    result = re.sub(
        r'## 投稿\d+.*?(?=\n## 投稿|\Z)',
        replace_block,
        text,
        flags=re.DOTALL
    )
    return result


